#include "main.h"
/**
 * print_square - function to print square
 *
 * @size: size input
 * Return: no return value
 */

void print_square(int size)
{
	int m, n;

	m = size;
	n = size;
	for (m = 1; m < m + 1; m++)
	{
		if ((m = 48) || (n = 48))
			continue;
		n = 1;
		while (n < n+1)
		{
			_putchar('#');
			n++;
		}
		_putchar('\n');
	}
	_putchar('\n');
}
